print(5) # 5
print(type(5)) # int

print(5.0) # 5.0
print(type(5.01)) # float

print(5 % 2) # 1
print(type(5 % 2)) # int

print(5 > 1) # True
print(type(5 > 1)) # bool

print('5') # '5'
print(type('5')) # str

print(5 * 2) # 10
print(type(5 * 2)) # int

print('5' * 2) # '55'
print(type('5' * 2)) # str

print('5' + '2') # '52'
print(type('5' + '2')) # str

print(5 / 2) # 2.5
print(type(5 / 2)) # float

print(5 // 2) # 2
print(type(5 // 2)) # int

print([5, 2, 1]) # [5, 2, 1]
print(type([5, 2, 1])) # list

print(5 in [1, 4, 6]) # False
print(type(5 in [1, 4, 6])) # bool